    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String an, me, di;
        an = anos.getText();
        me = meses.getText();
        di = dias.getText();
        
        int anosD = Integer.parseInt(an);
        int mesesD = Integer.parseInt(me);
        int diasD = Integer.parseInt(di);
        
        int totalD = (anosD * 365) + (mesesD * 30) + diasD;
        
        JOptionPane.showMessageDialog(this.getRootPane(), "Sua idade em dias Ã©: " + totalD, "Idade em dias", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

